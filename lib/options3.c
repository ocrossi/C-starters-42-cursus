#include "ft_printf.h"

void		scan_bout_de_scotch2(char **r3, int *cpt)
{
	while (*cpt > 0)
	{
		if (**r3 == '.')
		{
			*r3 -= 1;
			*cpt -= 1;
		}
		if (**r3 == '9')
			**r3 = '0';
		else
		{
			**r3 += 1;
			break ;
		}
		*cpt -= 1;
		*r3 -= 1;
	}
}
